/* SHOW/HIDE GRAD DETAILS ON CLICK OF PORTRAIT
–––––––––––––––––––––––––––––––––––––––––––––––––– */
function showGradDetails() {
	let grads = document.querySelectorAll('.grad')
	for (let i = 0; i < grads.length; i++) {
		let grad  = grads[i]
		let image = grad.querySelector('.grad-portrait img')
		image.addEventListener('click', function() {
			if (grad.classList.contains('grad-active')) {
				grad.classList.remove('grad-active')
			} else {
				if (document.querySelector('.grad-active')) {
					document.querySelector('.grad-active').classList.remove('grad-active')
				}
				grad.classList.add('grad-active')
			}
		})
	}
}



/* FILTER GRADS BY SPECIALIZATION
–––––––––––––––––––––––––––––––––––––––––––––––––– */
function filterGrads() {
	if (document.querySelector('.filters')) {
		let filtering = false
		let filters = document.querySelectorAll('.filters > div')
		let grads = document.querySelector('.grads')
		let container = document.querySelector('.grads-container')
		for (let i = 0; i < filters.length; i++) {
			let filter = filters[i]
			filter.addEventListener('click', function() {
				if (grads.classList.contains(filter.className)) {
					grads.classList.remove(filter.className)
					filter.id = ''
				} else {
					grads.className = 'grads'
					if (document.querySelector('#filter-active')) {
						let last = document.querySelector('#filter-active')
						last.id = ''
					}
					grads.classList.add(filter.className)
					filter.id = 'filter-active'

				}
				if (grads.classList.length > 1)  {
					grads.id = 'filtering'
					filtering = true

				} else {
					grads.id = ''
					filtering = false
				}
				if (filtering === true) {
					document.querySelector('.filters').id = 'filter-selected'
				} else {
					document.querySelector('.filters').id = ''
				}
				container.scrollTo(0,0)
				if (document.querySelector('.grad-active')) {
					document.querySelector('.grad-active').classList.remove('grad-active')
				}
			})
		}

	}
}

